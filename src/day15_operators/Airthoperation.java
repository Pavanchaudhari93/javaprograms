package day15_operators;

import java.net.StandardSocketOptions;

public class Airthoperation {

	public static void main(String[] args) {
		
		// Variables Definition and Initialization
		int num1 = 10 , num2 = 20;
		
		// Addition Operation
		int sumres = num1 + num2;
		System.out.println("Addition of two numbers is : "+ sumres);
		
		// Subtraction Operation
		int diffres = num1 - num2;
		System.out.println("Difference of two numbers is : "+diffres);
		
		// Multiplication Operation
		int mulres = num1 * num2;
		System.out.println("Mutiplication of two numbers is : "+mulres);
		
		// Division Operation
		int divres = num2 / num1;
		System.out.println("Divison of two numbers is : "+divres);

		// Modulus Operation
		int modres = num1 % num2;
		System.out.println("Modulus of two numbers is : "+modres);
	}

}
