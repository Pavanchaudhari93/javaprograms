package day15_operators;

public class Logicaloperationn {

	public static void main(String[] args) {
		boolean bool1=true , bool2 = true;
		
		System.out.println("Logical AND operation--> (bool1 && bool2) is : "+(bool1&&bool2));
		System.out.println("Logical OR operation --> (bool1 || bool2) is : "+(bool1||bool2));
		System.out.println("Logical NOT operation --> !(bool1 && bool2) is : "+!(bool1&&bool2));
	}

}
